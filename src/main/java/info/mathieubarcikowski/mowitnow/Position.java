package info.mathieubarcikowski.mowitnow;

public class Position
{
    private final int x;
    private final int y;


    public Position(int aX, int aY)
    {
        x = aX;
        y = aY;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }
}
